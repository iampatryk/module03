package LibraryMyLearning;

public class MyAuthor {
    String name;
    int age;
    String favouriteGenre;

    public MyAuthor(String name, int age, String favouriteGenre) {
        this.name = name;
        this.age = age;
        this.favouriteGenre = favouriteGenre;
    }

    public String getName() {
        return name;
    }
}
