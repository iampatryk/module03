package LibraryMyLearning;

public class String {
    java.lang.String name;
    int age;
    java.lang.String favouriteGenre;

    public String(java.lang.String name, int age, java.lang.String favouriteGenre) {
        this.name = name;
        this.age = age;
        this.favouriteGenre = favouriteGenre;
    }

    public java.lang.String getName() {
        return name;
    }
}
